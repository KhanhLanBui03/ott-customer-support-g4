// Helper to set nested object value by path
function setNestedValue(obj, path, value) {
  const parts = path.split('.');
  let current = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    const part = parts[i];
    if (!current[part] || typeof current[part] !== 'object') {
      current[part] = {};
    }
    current = current[part];
  }
  current[parts[parts.length - 1]] = value;
}

['vi', 'en'].forEach(lang => {
  const filePath = path.join(__dirname, `../src/locales/${lang}/translation.json`);
  if (!fs.existsSync(filePath)) {
    console.error(`File not found: ${filePath}`);
    return;
  }
  
  const translations = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  const keys = NEW_KEYS[lang];
  for (const pathKey in keys) {
    setNestedValue(translations, pathKey, keys[pathKey]);
  }
  
  fs.writeFileSync(filePath, JSON.stringify(translations, null, 2), 'utf8');
  console.log(`Updated mobile ${lang} translation file with new keys.`);
});
