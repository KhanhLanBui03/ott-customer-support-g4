const LANG_MAP = [
  { mobileCode: 'zh', webFolder: 'cn' },
  { mobileCode: 'ja', webFolder: 'jp' },
  { mobileCode: 'ko', webFolder: 'kr' },
  { mobileCode: 'fr', webFolder: 'fr' }
];

const mobileEn = JSON.parse(fs.readFileSync(path.join(__dirname, '../src/locales/en/translation.json'), 'utf8'));

// Helper to set nested object value by path
function setNestedValue(obj, path, value) {
  const parts = path.split('.');
  let current = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    const part = parts[i];
    if (!current[part] || typeof current[part] !== 'object') {
      current[part] = {};
    }
    current = current[part];
  }
  current[parts[parts.length - 1]] = value;
}

// Helper to deeply merge target into source (in place)
function deepMerge(source, target) {
  for (const key in target) {
    if (target[key] !== null && typeof target[key] === 'object' && !Array.isArray(target[key])) {
      if (!source[key] || typeof source[key] !== 'object') {
        source[key] = {};
      }
      deepMerge(source[key], target[key]);
    } else {
      source[key] = target[key];
    }
  }
}

// Generate each translation file
LANG_MAP.forEach(({ mobileCode, webFolder }) => {
  const webFilePath = path.join(__dirname, `../../web/src/locales/${webFolder}/translation.json`);
  if (!fs.existsSync(webFilePath)) {
    console.error(`Web translation file not found: ${webFilePath}`);
    return;
  }

  const webTranslationRoot = JSON.parse(fs.readFileSync(webFilePath, 'utf8'));
  // Web translation structure has a top level 'translation' wrapper
  const webTranslation = webTranslationRoot.translation || webTranslationRoot;

  // Clone mobileEn as base to preserve structure and missing keys
  const mobileTranslation = JSON.parse(JSON.stringify(mobileEn));

  // Merge web translation into clone
  deepMerge(mobileTranslation, webTranslation);

  // Apply extra overrides for mobile specific keys
  const extras = EXTRA_TRANSLATIONS[mobileCode] || {};
  for (const pathKey in extras) {
    setNestedValue(mobileTranslation, pathKey, extras[pathKey]);
  }

  // Create mobile folder if it does not exist
  const mobileFolderDir = path.join(__dirname, `../src/locales/${mobileCode}`);
  if (!fs.existsSync(mobileFolderDir)) {
    fs.mkdirSync(mobileFolderDir, { recursive: true });
  }

  // Write file
  const outPath = path.join(mobileFolderDir, 'translation.json');
  fs.writeFileSync(outPath, JSON.stringify(mobileTranslation, null, 2), 'utf8');
  console.log(`Generated: ${outPath}`);
});

